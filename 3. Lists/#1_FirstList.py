
# In python, a list is a collection of items that are ordered and changeable.
# Lists are written with square brackets `[]`, and the items in the list are separated by commas:

first_list = [1, 2, 3, 4, 5]

# Lists can contain any type of data, including numbers, strings, and even other lists.
# Lists are also mutable, meaning you can change their content without changing their identity:

first_list = [True, 2, 3.0, "Four", [5, 6, 7]]

# And we can print it simply by using the print() function:
print(first_list) # Output: [True, 2, 3.0, 'Four', [5, 6, 7]]

# We can also create an empty list and add items to it later:
empty_list = []